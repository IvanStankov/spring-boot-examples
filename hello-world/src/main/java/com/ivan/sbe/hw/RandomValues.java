package com.ivan.sbe.hw;

public class RandomValues {

    private int intValue;
    private long longValue;
    private String secret;
    private String uuid;
    private int lessThan20;
    private int range300_400;

    public int getIntValue() {
        return intValue;
    }

    public void setIntValue(int intValue) {
        this.intValue = intValue;
    }

    public long getLongValue() {
        return longValue;
    }

    public void setLongValue(long longValue) {
        this.longValue = longValue;
    }

    public String getSecret() {
        return secret;
    }

    public void setSecret(String secret) {
        this.secret = secret;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public int getLessThan20() {
        return lessThan20;
    }

    public void setLessThan20(int lessThan20) {
        this.lessThan20 = lessThan20;
    }

    public int getRange300_400() {
        return range300_400;
    }

    public void setRange300_400(int range300_400) {
        this.range300_400 = range300_400;
    }
}
